// Function to get skills by category
export const getSkillsByCategory = (category: string): Skill[] => {
  return skillsData.filter(skill => skill.category === category);
};

// Function to get all unique categories
export const getSkillCategories = (): string[] => {
  return [...new Set(skillsData.map(skill => skill.category))].sort();
};

// Function to search skills by name
export const searchSkills = (query: string): Skill[] => {
  const lowercaseQuery = query.toLowerCase();
  return skillsData.filter(skill => 
    skill.name.toLowerCase().includes(lowercaseQuery)
  );
};